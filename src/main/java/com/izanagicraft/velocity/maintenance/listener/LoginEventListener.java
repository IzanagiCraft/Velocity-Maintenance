package com.izanagicraft.velocity.maintenance.listener;

import com.izanagicraft.velocity.maintenance.VelocityMaintenancePlugin;
import com.izanagicraft.velocity.maintenance.messages.MessageUtils;
import com.velocitypowered.api.event.PostOrder;
import com.velocitypowered.api.event.ResultedEvent;
import com.velocitypowered.api.event.Subscribe;
import com.velocitypowered.api.event.connection.LoginEvent;
import com.velocitypowered.api.proxy.Player;

/**
 * Velocity-Maintenance; com.izanagicraft.velocity.maintenance.listener:LoginEventListener
 *
 * @author <a href="https://github.com/sanguine6660">@sanguine6660</a>
 * @since 16.12.2023
 */
public class LoginEventListener {

    private final VelocityMaintenancePlugin plugin;

    public LoginEventListener(VelocityMaintenancePlugin plugin) {
        this.plugin = plugin;
    }


    @Subscribe(order = PostOrder.FIRST)
    public void onPlayerConnect(final LoginEvent event) {
        if (!plugin.getMaintenanceData().get().isEnabled()) return;
        if (event.getPlayer() == null) return;
        Player player = event.getPlayer();
        String permission = String.valueOf(plugin.getConfig().getString("permissions.join"));
        if (permission == null || permission.isEmpty() || !player.hasPermission(permission)) {
            MessageUtils.kickPlayerForMaintenance(player);
        }
    }

}
