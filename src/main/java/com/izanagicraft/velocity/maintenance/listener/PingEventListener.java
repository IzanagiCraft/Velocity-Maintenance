package com.izanagicraft.velocity.maintenance.listener;

import com.izanagicraft.velocity.maintenance.VelocityMaintenancePlugin;
import com.izanagicraft.velocity.maintenance.messages.MessageUtils;
import com.velocitypowered.api.event.PostOrder;
import com.velocitypowered.api.event.Subscribe;
import com.velocitypowered.api.event.proxy.ProxyPingEvent;
import com.velocitypowered.api.proxy.server.ServerPing;

/**
 * Velocity-Maintenance; com.izanagicraft.velocity.maintenance.listener:PingEventListener
 *
 * @author <a href="https://github.com/sanguine6660">@sanguine6660</a>
 * @since 16.12.2023
 */
public class PingEventListener {
    private final VelocityMaintenancePlugin plugin;

    public PingEventListener(VelocityMaintenancePlugin plugin) {
        this.plugin = plugin;
    }

    @Subscribe(order = PostOrder.FIRST)
    public void onProxyPing(final ProxyPingEvent event) {
        if (!plugin.getMaintenanceData().get().isEnabled()) return;

        final ServerPing ping = event.getPing();
        final ServerPing.Builder builder = ping.asBuilder();

        String prefix = VelocityMaintenancePlugin.getInstance().getConfig().getString("plugin.prefix", "[M]");

        String version = plugin.getConfig().getString("ping.version");
        if (version != null && !version.isEmpty()) {
            builder.version(new ServerPing.Version(0, version));
        }

        String motd = plugin.getConfig().getString("ping.motd");
        if (motd != null && !motd.isEmpty()) {
            builder.description(
                    MessageUtils.getComponentSerializer().deserialize(
                            motd.replace("%prefix%", prefix)
                    )
            );
        }

        if ((version != null && !version.isEmpty()) || (motd != null && !motd.isEmpty())) {
            event.setPing(builder.build());
        }
    }
}
